    <h1>Kind Valentine</h1>

    <p>
      She hugs a white rose to her heart—<br />
      The petals flare—in her breath blown;<br />
      She'll catch the fruit on her death day—<br />
      The flower rooted in the bone.<br />
      The face at evening comes for love;<br />
      Reeds in the river meet below.<br />
      She sleeps small child, her face a tear;<br />
      The dream comes in with stars to go<br />
      Into the window, feigning snow.<br />
      This is the book that no one knows.<br />
      The paper wall holds mythic oaks,<br />
      Behind the oaks a castle grows.<br />
      Over the door, and over her<br />
      (She dies! she wakes!) the steeds gallop.<br />
      The child stirs, hits the dumb air, weeps,<br />
      Afraid of night's long loving-cup.
    </p>

    <p>
      Into yourself, live, Joanne!<br />
      And count the buttons—how they run<br />
      To doctor, red chief, lady's man!<br />
      Most softly pass, on the stairs down,<br />
      The stranger in your evening gown.<br />
      Hearing white, inside your grief,<br />
      An insane laughter up the roof.<br />
      O little wind, come in with dawn—<br />
      It is your shadow on the lawn.
    </p>

    <p>
      Break the pot! and let carnations—<br />
      Smell them! they're the very first.<br />
      Break the sky and let come magic<br />
      Rain! Let earth come pseudo-tragic<br />
      Roses—blossom, unrehearsed.
    </p>

    <p>
      Head, break! is broken. Dream, so small,<br />
      Come in to her. O little child,<br />
      Dance on squills where the winds run wild.
    </p>

    <p>
      The candles rise in the warm night<br />
      Back and forth, the tide is bright.<br />
      Slowly, slowly, the waves retreat<br />
      Under her wish and under feet.<br />
      And over tight breath, tighter eyes,<br />
      The mirror ebbs, it ebbs and flows.<br />
      And the intern, the driver, speed<br />
      To gangrene! But—who knows—suppose<br />
      He was beside her! Please, star-bright,<br />
      First I see, while in the night<br />
      A soft-voiced, like a tear, guitar—<br />
      It calls a palm coast from afar.<br />
      And oh, so far the stars were there<br />
      For him to hang upon her hair<br />
      Like the white rose he gave, white hot,<br />
      While the low sobbing band—it wept<br />
      Violets and forget-me-nots.
    </p>

