    <h1>The Visitor</h1>

    <p>
      He came from the mountains into this<br />
      Garden. Welcome, sir, all that I have is yours.
    </p>

    <p>
      He came from the mountains, he spied<br />
      The kind shade. He sate with me under the oak tree.
    </p>

    <p>
      What have you done in the mountains, sir,<br />
      Besides hunting the white deer all day?
    </p>

    <p>
      In the mountains I hunted and I plotted<br />
      Your garden's destruction and ruin.
    </p>

    <p>
      In the mountains I hunted a similitude<br />
      To obtain your trusting mood.
    </p>

    <p>
      Therefore I slay you as<br />
      You dream of the friendship I bear.
    </p>

    <p>
      Said the man from the mountains the mountains<br />
      Who came to visit me here.
    </p>

    <p>
      But I shall, as I look only upward<br />
      My star being set in the mountains,
    </p>

    <p>
      Said the man, the man from the mountains,<br />
      See only the fair garden that I murdered here
    </p>

    <p>Said the man the man from the mountains.</p>

